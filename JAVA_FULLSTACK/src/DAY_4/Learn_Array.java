package DAY_4;
import java.util.Arrays;

public class Learn_Array {
	public static void main(String[] args) {

        // initializing array
        int[] arr = { 40,55,63,17,22,68,89,97,89};

        // size of array
        int n = arr.length;
        System.out.println("array length=" + n);


        // traversing array
        for (int i = 0; i < n; i++)
            System.out.print(arr[i] + " ");
    }


}
