package DAY_2;

public class UnaryOperator {
	public static void main(String[]args ) 

	{ 
		int a=10 ; 
		int b=20 ;
		
		System.out.println("++a="+(++a)); //11
		System.out.println("−−a="+(--a)); //10
		System.out.println("++b="+(++b));//21
		System.out.println("−−b="+(--b)); //20
}
}


