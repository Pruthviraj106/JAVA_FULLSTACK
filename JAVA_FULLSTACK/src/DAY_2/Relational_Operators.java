package DAY_2;

public class Relational_Operators {
	/**
	 * @param args
	 */
	public static void main(String[] args) { 

		int a=10 ; 
		int b=20; 

		System.out.println("a==b= "+(a==b) ); 

		System.out.println("a!=b= "+(a!=b)); 
		
		System.out.println("a>b= "+(a>b) ); 

		System.out.println("a<b= "+(a<b) ); 

		System.out.println("a>=b= "+(a>=b) ); 

		System.out.println("a<=b= "+(a<=b) ); 


		

		} 

		

}
